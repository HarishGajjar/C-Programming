#include <stdio.h>  // Includes the standard input/output library for using printf()

int main() {
    char grade = 'A';  // Declare a character variable and assign it the value 'A'

    // Print the value of the character variable 'grade' using %c format specifier
    printf("Student grade: %c\n", grade);

    return 0;  // Return 0 to indicate successful program execution
}

